// Generates a realistic curvy price path
function generatePrices(start, count, volatility, trend) {
  const prices = [start];
  for (let i = 1; i < count; i++) {
    const change = (Math.random() - 0.48) * volatility + trend;
    prices.push(Math.max(1, prices[i - 1] + change));
  }
  return prices;
}

