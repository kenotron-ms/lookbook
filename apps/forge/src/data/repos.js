// 52 weeks × 7 days of contribution data (0–4 intensity)
export const CONTRIBUTION_DATA = (() => {
  const weeks = []
  const seed = [0,0,0,1,2,1,0, 0,1,2,3,2,1,0, 0,0,1,2,1,0,0, 1,2,3,4,3,2,1]
  for (let w = 0; w < 52; w++) {
    const week = []
    for (let d = 0; d < 7; d++) {
      const base = seed[(w * 7 + d) % seed.length]
      const noise = Math.floor(Math.abs(Math.sin(w * 13 + d * 7) * 3))
      // More activity in recent months (last 26 weeks)
      const recency = w >= 26 ? 1 : 0
      const val = Math.min(4, base + (noise % 2) + recency)
      week.push(val)
    }
    weeks.push(week)
  }
  return weeks
})()

